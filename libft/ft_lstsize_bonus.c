#include "libft.h"

int	ft_lstsize(t_list *lst)
{
	int	i;

	i = 0;
	while (lst != 0)
	{
		i++;
		if (lst -> next)
			lst = lst -> next;
		else
			break ;
	}
	return (i);
}
