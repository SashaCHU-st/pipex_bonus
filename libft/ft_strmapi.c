#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	size_t	i;
	size_t	length;
	char	*new;

	length = 0;
	i = 0;
	if (!s)
		return (NULL);
	length = ft_strlen(s);
	new = (char *) malloc(sizeof(char) * (length + 1));
	if (!new)
		return (NULL);
	while (s[i] != '\0')
	{
		new[i] = f(i, s[i]);
		i++;
	}
	new[i] = '\0';
	return (new);
}
